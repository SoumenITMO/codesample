package com.samasa.hspms.common.dto;

public class FormControlConditions {
	
	private String ctrlCode;
	private Integer ctrlValueNum;
	private String ctrlValueChar;
	private String ctrlValueNumCondition;
	private String ctrlValueCharCondition;
	
	public String getCtrlCode() {
		return ctrlCode;
	}
	public void setCtrlCode(String ctrlCode) {
		this.ctrlCode = ctrlCode;
	}
	public Integer getCtrlValueNum() {
		return ctrlValueNum;
	}
	public void setCtrlValueNum(Integer ctrlValueNum) {
		this.ctrlValueNum = ctrlValueNum;
	}
	public String getCtrlValueChar() {
		return ctrlValueChar;
	}
	public void setCtrlValueChar(String ctrlValueChar) {
		this.ctrlValueChar = ctrlValueChar;
	}
	public String getCtrlValueNumCondition() {
		return ctrlValueNumCondition;
	}
	public void setCtrlValueNumCondition(String ctrlValueNumCondition) {
		this.ctrlValueNumCondition = ctrlValueNumCondition;
	}
	public String getCtrlValueCharCondition() {
		return ctrlValueCharCondition;
	}
	public void setCtrlValueCharCondition(String ctrlValueCharCondition) {
		this.ctrlValueCharCondition = ctrlValueCharCondition;
	}

}
